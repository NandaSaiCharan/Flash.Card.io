let currentIndex = 0;

const flashcard = document.getElementById('flashcard');
const nextCardButton = document.getElementById('nextCard');
const prevCardButton = document.getElementById('prevCard');

function showCard(index) {
    const frontText = words[index].front;
    const backText = words[index].back;
    document.querySelector('.front').textContent = frontText;
    document.querySelector('.back').textContent = backText;
}

flashcard.addEventListener('click', () => {
    flashcard.classList.toggle('flipped');
});

nextCardButton.addEventListener('click', () => {
    currentIndex = (currentIndex + 1) % words.length;
    showCard(currentIndex);
    flashcard.classList.remove('flipped'); // Reset flip when moving to next card
});

prevCardButton.addEventListener('click', () => {
    currentIndex = (currentIndex - 1 + words.length) % words.length; // Wrap around
    showCard(currentIndex);
    flashcard.classList.remove('flipped'); // Reset flip when moving to previous card
});

showCard(currentIndex);
